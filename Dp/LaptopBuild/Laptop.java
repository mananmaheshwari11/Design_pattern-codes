package LaptopBuild;

public class Laptop {
    String ram;
    String processor;
    String touchPad;
    String screen;
    
    public void setRam(String ram){
            this.ram=ram;
    }
    public void setProcessor(String p){
        this.processor=p;
    }
    public void setTouchpad(String touch){
        this.touchPad=touch;
    }
    public void setScreen(String screen){
        this.screen=screen;
    }
}
